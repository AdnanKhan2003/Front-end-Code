// Data Type:
/*1. Primitive Data Type: Number,String,Null,Undefined,Boolean,Symbol*/

// 1. Number
let a = 7;
console.log(typeof a);

// 2. String
let b = 'Dont bother reading this';
console.log(typeof b);

// 3. Undefined
let c;
console.log(typeof c);

// 4. Null
let d = null;
console.log(d);

// 5. Boolean
let e = 9>7;
let f = 5>9;
console.log(e);
console.log(f);

//2. Preference Data Type: Object,Arrays

// 1. Arrays
let g = ['Adnan','khan'];
console.log(g);

// 2. Object
let h = {Name: 'Adnan',Age:45};
console.log(h);
